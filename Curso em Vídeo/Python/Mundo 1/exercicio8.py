'''
Escreva um programa que leia um valor em metros e o exiba convertido em centímetro s e milímetros.
'''

metros = float(input('Digite um valor em metros: '))
centimetros = metros * 100
milimilímetros = metros * 1000
print(f'O valor de {metros} metros convertido em centímetros é {centimetros} e em milímetros é {milimilímetros}.')